# Atribuição Simples
saldo = 500
print(saldo)

saldo = 200
print(saldo)

# Atribuição Com Adição
saldo += 10
print(saldo)

# Atribuição Com Subtração
saldo -= 5
print(saldo)

# Atribuição Com Divisão
saldo //= 2
print(saldo)

saldo /= 2
print(saldo)

# Atribuição Com Mutiplicação
saldo *= 10
print(saldo)

# Atribuição com módulo 
saldo %= 4
print(saldo)

# Atribuição com exponenciação
saldo **= 2
print(saldo)
